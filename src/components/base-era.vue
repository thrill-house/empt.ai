<script>
import svg from '../svg';
import { mapGetters } from 'vuex';

export default {
  name: 'base-era',
  blockName: 'era',
  props: {
    label: String,
  },
  computed: {
    stage: function() {
      return this.getStage(this.label);
    },
    total: function() {
      return this.getStages();
    },
    ...mapGetters(['getStage', 'getStages']),
  },
};
</script>

<template>
  <div v-bem>
    <span
      v-for="n in total"
      v-bem:stage="{ color: stage >= n ? 'sky' : 'grey' }"
    ></span>
  </div>
</template>
